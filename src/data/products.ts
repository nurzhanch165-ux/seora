export function getProduct(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function productsBySection(sectionSlug: string): Product[] {
  return products.filter((p) => p.sectionSlug === sectionSlug);
}

export function productsByCategory(sectionSlug: string, categorySlug: string): Product[] {
  return products.filter(
    (p) => p.sectionSlug === sectionSlug && p.categorySlug === categorySlug
  );
}

export function relatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter((p) => p.id !== product.id && p.categorySlug === product.categorySlug)
    .concat(
      products.filter(
        (p) => p.id !== product.id && p.sectionSlug === product.sectionSlug
      )
    )
    .filter((p, i, arr) => arr.findIndex((x) => x.id === p.id) === i)
    .slice(0, limit);
}

